import UIKit

extension UIColor {
    class var dhlBlackTransparent: UIColor {
        return UIColor(red: 0/255, green: 0/255, blue: 0/255, alpha: 0.1)
    }
    class var dhlYellow: UIColor {
        return UIColor(red: 255/255, green: 204/255, blue: 0/255, alpha: 1.0)
    }
    class var dhlRed: UIColor {
        return UIColor(red: 212/255, green: 5/255, blue: 17/255, alpha: 1.0)
    }

}
